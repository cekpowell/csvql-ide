package View;

/**
 * Entry point for application. Simply calls the main method of 'Main.java'.
 * 
 * Needed in order to build executable jar file for a JavaFX project.
 */
public class Entry {

    /**
     * Main method - invokes the main method of the Main class.
     * 
     * @param args System arguments.
     */
    public static void main(String[] args) {
        Main.main(args);
    }
}